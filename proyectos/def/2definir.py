

def suma(numero1,numero2):
    print(numero1+numero2)
    print("Estoy sumando en una funcion")


def multiplicacion(numero1,numero2):
    print(numero1*numero2)
    print("Estoy multiplicando en una funcion")


def multiplicacion3(numero1,numero2,numero3):
    print(numero1*numero2*numero3)
    print("Estoy multiplicando 3 numeros en una funcion")

def division(numero1,numero2):
    print(numero1/numero2)
    print("Estoy dividiendo en una funcion")

suma(2,2)

suma(10,20)

multiplicacion(2,2)

multiplicacion(10,20)

multiplicacion3(3,4,5)

multiplicacion3(5,6,7)

